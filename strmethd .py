name = "DhEeraJ MalI"

#1. len() function
print(len(name)) #output = 20


#2. lower() method #in method we use (.)=dot
print(name.lower())
#output = sarthak gagapalliwar all in small character


#3. upper() method
print(name.upper())
#output=SARTHAK GAGAPALLIWAR all character


#4. title() method
print(name.title())
#output= Sarthak Gagapalliwar all word first letter name capital


#5. count() method
print(name.count("a")) #output = 5 
#count the number how much in the word 
